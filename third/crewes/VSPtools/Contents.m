%VSPtools contains a set of functions desgined to work with VSPs
%
% VSP_pick ... picks the first breaks on a VSP
% VSP_flatten ... flatten an event with known traveltimes (typically the first breaks)
% VSP_separation ... separate a VSP into upgoing and downgoing wavefields
% VSP_medfilt ... apply spatial median filtering to a flattened VSP
% VSP_q ... measure Q on the separated downgoing VSP wavefield
% VSP_decon ... deconvolve the VSP upgoing field using the downgoing field
% 